#include "3-calc.h"

/**
 * op_add - function add
 * @Number1: Integer 1
 * @Number2: Integer 2
 * Return: add
 */

int op_add(int Number1, int Number2)
{
	return (Number1 + Number2);
}

/**
 * op_sub - function subtraction
 * @Number1: Integer 1
 * @Number2: Integer 2
 * Return: subtraction
 */

int op_sub(int Number1, int Number2)
{
	return (Number1 - Number2);
}

/**
 * op_mul - function multiplication
 * @Number1: Integer 1
 * @Number2: Integer 2
 * Return: Multiplication.
 */

int op_mul(int Number1, int Number2)
{
	return (Number1 * Number2);
}

/**
 * op_div - function divide
 * @Number1: Integer 1
 * @Number2: Integer 2
 * Return: Divide.
 */

int op_div(int Number1, int Number2)
{
	return (Number1 / Number2);
}

/**
 * op_mod - function module
 * @Number1: Integer 1
 * @Number2: Integer 2
 * Return: Module.
 */

int op_mod(int Number1, int Number2)
{
	return (Number1 % Number2);
}
